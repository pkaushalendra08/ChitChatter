import { createContext, useContext, useEffect, useState } from "react";
import { AuthContext } from "./AuthContext";
import toast from "react-hot-toast";

export const ChatContext = createContext();

export const ChatProvider = ({ children }) => {
    const [messages, setMessages] = useState([]);
    const [users, setUsers] = useState([]);
    const [selectedUser, setSelectedUser] = useState(null);
    const [unseenMessages, setUnseenMessages] = useState({});

    const { socket, axios, backendUrl } = useContext(AuthContext);

    // Fetch all users for sidebar
    const getUsers = async () => {
        try {
            const { data } = await axios.get(`${backendUrl}/api/messages/users`);
            if (data.success) {
                setUsers(data.users);
                setUnseenMessages(data.unseenMessages);
            }
        } catch (error) {
            toast.error(error.message);
        }
    };

    // Fetch messages from a selected user
    const getMessages = async (userId) => {
        try {
            const { data } = await axios.get(`${backendUrl}/api/messages/${userId}`);
            if (data.success) setMessages(data.messages);
        } catch (error) {
            toast.error(error.message);
        }
    };

    // Send message to selected user
    const sendMessage = async (messageData) => {
        if (!selectedUser) return;

        try {
            const { data } = await axios.post(
                `${backendUrl}/api/messages/send/${selectedUser._id}`,
                messageData
            );
            if (data.success) setMessages((prev) => [...prev, data.newMessage]);
            else toast.error(data.message);
        } catch (error) {
            toast.error(error.message);
        }
    };

    // Subscribe to real-time messages
    useEffect(() => {
        if (!socket) return;

        const handleNewMessage = (newMessage) => {
            if (selectedUser && newMessage.senderId === selectedUser._id) {
                newMessage.seen = true;
                setMessages((prev) => [...prev, newMessage]);
                axios.put(`${backendUrl}/api/messages/mark/${newMessage._id}`);
            } else {
                setUnseenMessages((prev) => ({
                    ...prev,
                    [newMessage.senderId]: prev[newMessage.senderId]
                        ? prev[newMessage.senderId] + 1
                        : 1,
                }));
            }
        };

        socket.on("newMessage", handleNewMessage);

        return () => socket.off("newMessage", handleNewMessage);
    }, [socket, selectedUser]);

    const value = {
        messages,
        users,
        selectedUser,
        getUsers,
        setMessages,
        sendMessage,
        setSelectedUser,
        unseenMessages,
        setUnseenMessages,
        getMessages,
    };

    return <ChatContext.Provider value={value}>
        {children}
    </ChatContext.Provider>;
};
